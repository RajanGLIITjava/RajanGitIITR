/*Write a program in java to print the following pattern
(Hint : Use nested for loop)
*
**
***
****
*****
*/
package preclasspractice19102022;
public class JavaNestedLooppractice {
	public static void main(String[] args) {
	for(int i=5;i>=5;i--);
	System.out.println("*****");
	for(int i=4;i>=4;i--);
	System.out.println("****");
	for(int i=3;i>=3;i--);
	System.out.println("***");
	for(int i=2;i>=2;i--);
	System.out.println("**");
	for(int i=1;i>=1;i--);
	System.out.println("*");
	}
}
